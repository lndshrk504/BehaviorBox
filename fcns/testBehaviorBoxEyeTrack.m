function socket = openSubscriber_(address, rcvhwm)
socket = struct('Address', string(address), 'RcvHwm', double(rcvhwm));
state = getappdata(0, 'BBEyeTrackTestBridgeState');
state.Opened = true;
setappdata(0, 'BBEyeTrackTestBridgeState', state);
end

function jsonText = recvLatestJson_(~, ~)
state = getappdata(0, 'BBEyeTrackTestBridgeState');
state.NextIndex = state.NextIndex + 1;
if state.NextIndex <= numel(state.Messages)
    jsonText = string(state.Messages{state.NextIndex});
else
    jsonText = "";
end
setappdata(0, 'BBEyeTrackTestBridgeState', state);
end

function closeSocket_(~)
state = getappdata(0, 'BBEyeTrackTestBridgeState');
state.Closed = true;
setappdata(0, 'BBEyeTrackTestBridgeState', state);
end
