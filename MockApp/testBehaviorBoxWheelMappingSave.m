assert(all(ismember(["mode", "level", "variant", "x", "y", "angleDeg", "scale", "brightness", ...
    "eye_center_x", "eye_center_y", "eye_sample_count"], frameVars)), ...
    'FrameAlignedRecord is missing mapping state or aligned-eye columns.');

