assert(loaded.EyeAlignedRecord.screenEvent_count(2) == 1, 'Second eye sample should capture one screen event in its interval.');
assert(loaded.EyeAlignedRecord.screenEvent(2) == "FlashOn", 'Second eye sample should align FlashOn to the eye-backed table.');
assert(contains(loaded.EyeAlignedRecord.screenEvent(3), "RewardGiven"), 'Third eye sample should include RewardGiven.');
assert(contains(loaded.EyeAlignedRecord.screenEvent(3), "FlashOff"), 'Third eye sample should include FlashOff.');
assert(loaded.EyeAlignedRecord.phase(2) == "flash", 'Second eye sample should be labeled as flash phase.');
