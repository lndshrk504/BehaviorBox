assert(loaded.FrameAlignedRecord.eye_center_x(2) == 30.0, 'Second frame should align to the most recent previous eye sample.');
assert(loaded.FrameAlignedRecord.phase(1) == "flash", 'First frame should be labeled as flash phase.');
assert(loaded.FrameAlignedRecord.phase(2) == "interflash", 'Second frame should be labeled as interflash phase.');
