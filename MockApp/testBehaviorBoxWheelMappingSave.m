function jsonText = makeEyeJson_(sessionStartWallClock, offsetUs, frameId, centerX, pointOffset)
captureTimeUnixS = posixtime(sessionStartWallClock) + (double(offsetUs) / 1e6);
publishTimeUnixS = captureTimeUnixS + 0.100000;
captureTimeUnixNs = sprintf('%.0f', captureTimeUnixS * 1e9);
publishTimeUnixNs = sprintf('%.0f', publishTimeUnixS * 1e9);
centerY = centerX + 0.5;
diameterPx = 30.0 + (pointOffset / 10.0);
diameterHPx = diameterPx + 1.1;
diameterVPx = diameterPx - 1.1;
jsonText = [
    "{", ...
    """message_type"":""sample"",", ...
    """schema_version"":1,", ...
    """sample_status"":""ok"",", ...
    """source"":""dlc_eye_streamer"",", ...
    """model_preset"":""yanglab-pupil8"",", ...
    """model_type"":""base"",", ...
    """frame_id"":" + string(frameId) + ",", ...
    """capture_time_unix_s"":" + string(sprintf('%.6f', captureTimeUnixS)) + ",", ...
    """capture_time_unix_ns"":" + string(captureTimeUnixNs) + ",", ...
    """publish_time_unix_s"":" + string(sprintf('%.6f', publishTimeUnixS)) + ",", ...
    """publish_time_unix_ns"":" + string(publishTimeUnixNs) + ",", ...
    """camera_fps"":120.0,", ...
    """inference_fps"":60.0,", ...
    """latency_ms"":9.2,", ...
    """center_x"":" + string(sprintf('%.1f', centerX)) + ",", ...
    """center_y"":" + string(sprintf('%.1f', centerY)) + ",", ...
    """diameter_px"":" + string(sprintf('%.1f', diameterPx)) + ",", ...
    """diameter_h_px"":" + string(sprintf('%.1f', diameterHPx)) + ",", ...
    """diameter_v_px"":" + string(sprintf('%.1f', diameterVPx)) + ",", ...
    """confidence_mean"":0.97,", ...
    """valid_points"":8,", ...
    """point_names"":[""Lpupil"",""LDpupil"",""Dpupil"",""DRpupil"",""Rpupil"",""RVpupil"",""Vpupil"",""VLpupil""],", ...
    """point_count"":8,", ...
    """points"":{", ...
    """Lpupil"":[" + string(pointOffset + 1) + "," + string(pointOffset + 2) + ",0.91],", ...
    """LDpupil"":[" + string(pointOffset + 2) + "," + string(pointOffset + 3) + ",0.92],", ...
    """Dpupil"":[" + string(pointOffset + 3) + "," + string(pointOffset + 4) + ",0.93],", ...
    """DRpupil"":[" + string(pointOffset + 4) + "," + string(pointOffset + 5) + ",0.94],", ...
    """Rpupil"":[" + string(pointOffset + 5) + "," + string(pointOffset + 6) + ",0.95],", ...
    """RVpupil"":[" + string(pointOffset + 6) + "," + string(pointOffset + 7) + ",0.96],", ...
    """Vpupil"":[" + string(pointOffset + 7) + "," + string(pointOffset + 8) + ",0.97],", ...
    """VLpupil"":[" + string(pointOffset + 8) + "," + string(pointOffset + 9) + ",0.98]", ...
    "}", ...
    "}"];
jsonText = join(jsonText, "");
end
